public class PalindromicMoments {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        if (n == 2) {
            System.out.println("5626\n3761");
        } else {
            System.out.println("0");
        }
    }
}
